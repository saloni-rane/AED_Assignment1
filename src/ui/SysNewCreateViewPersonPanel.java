    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
        String Member = FieldMember.getSelectedItem().toString();
        String FName = FieldFirstName.getText();
        String LName = FieldLastName.getText();
        int Primary_ID = Integer.parseInt(FieldPrimaryID.getText());
        int Age = Integer.parseInt(FieldAge.getText());
        String Email = FieldEmail.getText();
        String Gender = FieldGender.getText();
        String Password = FieldPassword.getText();
        String City = FieldCity.getSelectedItem().toString();
        String Community = FieldCommunity.getSelectedItem().toString();
        String Address = FieldAddress.getText();
        int Zipcode = Integer.parseInt(FieldZipcode.getText());
        int HouseNumber = Integer.parseInt(FieldHouseNumber.getSelectedItem().toString());
        
        Person p = personhistory.addNewPerson();

            p.setMember(Member);
            p.setFirst_Name(FName);
            p.setLast_Name(LName);
            p.setEmail(Email);
            p.setAge(Age);
            p.setGender(Gender);
            p.setPrimary_ID(Primary_ID);
            p.setPassword(Password);
            p.setCity(City);
            p.setCommunity(Community);
            p.setAddress(Address);
            p.setZip_Code(Zipcode);
            p.setHouse_Number(HouseNumber);
            
        
        JOptionPane.showMessageDialog(this, "Person Information Saved");
        
            TablePersonInfodisplay();
            
            FieldMember.setSelectedItem("");
            FieldFirstName.setText("");
            FieldLastName.setText("");
            FieldEmail.setText("");
            FieldAge.setText("");
            FieldGender.setText("");
            FieldPrimaryID.setText("");
            FieldPassword.setText("");
            FieldCity.setSelectedItem("");
            FieldCommunity.setSelectedItem("");  
            FieldAddress.setText(""); 
            FieldZipcode.setText(""); 
        
    }//GEN-LAST:event_btnCreateActionPerformed

    private void FieldEmailKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_FieldEmailKeyReleased
        // TODO add your handling code here:
        if(FieldEmail.getText().trim() != "")
        {
            Pattern pat = Pattern.compile("^[A-Za-z0-9+_.-]+@(.+)$");
            Matcher matchEmailId= pat.matcher(FieldEmail.getText());
            if(!matchEmailId.matches())
            {
                lblEmailError.setText("Enter Valid Email");
            }
            else
            {
                lblEmailError.setText(null);
            }
        }
    }//GEN-LAST:event_FieldEmailKeyReleased

